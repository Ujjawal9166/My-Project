/*wjp to print number respective to each day in week */

class Switch
{
public static void main(String [] args)
{
String day="sunday";

switch(day){
case "sunday":
System.out.println(1);
break;
case "monday":
System.out.println(2);
break;
case "tuesday":
System.out.println(3);
break;
default:
System.out.println("invalid");
}
}
}
